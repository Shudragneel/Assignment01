#Write a  program to enter length in centimeter and convert it into meter and kilometer.

lcm=float(input("enter the length in cm:"))
#length in meter
lm=(lcm/100)
#length in kilometer
lkm=(lcm/100000)
print("length in meter=%2f" %lm)
print("length in kilometer=%2f" %lkm)




